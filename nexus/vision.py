@dataclass
class FaceProfile:
    """Stored face recognition profile."""
    profile_id: str = field(default_factory=lambda: str(uuid.uuid4()))
    user_name: str = ""
    face_encodings: List[List[float]] = field(default_factory=list)
    enrollment_count: int = 0
    created_at: datetime = field(default_factory=datetime.utcnow)
    last_seen: Optional[datetime] = None
    trust_level: int = 1  # 1-5 trust scale
    is_authorized: bool = True
    metadata: Dict[str, Any] = field(default_factory=dict)

    def add_encoding(self, encoding: List[float]) -> None:
        self.face_encodings.append(encoding)
        self.enrollment_count += 1

    def get_average_encoding(self) -> List[float]:
        if not self.face_encodings:
            return []
        dim = len(self.face_encodings[0])
        avg = [0.0] * dim
        for enc in self.face_encodings:
            for i in range(dim):
                avg[i] += enc[i]
        return [v / len(self.face_encodings) for v in avg]

    def to_dict(self) -> Dict[str, Any]:
        return {
            "profile_id": self.profile_id,
            "user_name": self.user_name,
            "enrollment_count": self.enrollment_count,
            "created_at": self.created_at.isoformat(),
            "last_seen": self.last_seen.isoformat() if self.last_seen else None,
            "trust_level": self.trust_level,
            "is_authorized": self.is_authorized,
        }


