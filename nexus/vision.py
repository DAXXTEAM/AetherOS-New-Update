# Lockdown Manager
# ═══════════════════════════════════════════════════════════════════════════

class LockdownManager:
    """Manages system lockdown based on presence detection events.

    Implements graduated lockdown levels with configurable timeouts
    and automatic escalation when threats persist.

    Lockdown escalation:
        NONE → SOFT (operator absent 30s) → MEDIUM (absent 2min)
        → HARD (unauthorized detected) → CRITICAL (persistent threat)
    """

    def __init__(
        self,
        soft_timeout: float = 30.0,
        medium_timeout: float = 120.0,
        auto_escalate: bool = True,
        on_lockdown_change: Optional[Callable[[LockdownLevel, LockdownLevel], None]] = None,
    ):
        self.soft_timeout = soft_timeout
        self.medium_timeout = medium_timeout
        self.auto_escalate = auto_escalate
        self._on_change = on_lockdown_change
        self._current_level = LockdownLevel.NONE
        self._level_since: float = time.time()
        self._absence_start: Optional[float] = None
        self._threat_detected_at: Optional[float] = None
        self._history: deque = deque(maxlen=200)
        self._lock = threading.Lock()
        self._actions_log: List[Dict[str, Any]] = []
        logger.info("LockdownManager initialized")

    def on_presence_event(self, event: PresenceEvent) -> None:
        """Handle a presence event and adjust lockdown level."""
        now = time.time()

        with self._lock:
            old_level = self._current_level

            if event.state_after == PresenceState.PRESENT:
                if event.recognized_users:
                    # Known user present — can reduce lockdown
                    self._absence_start = None
                    self._threat_detected_at = None
                    if self._current_level.value <= LockdownLevel.MEDIUM.value:
                        self._set_level(LockdownLevel.NONE)
                else:
                    # Unknown face present
                    self._threat_detected_at = now
                    if self._current_level.value < LockdownLevel.HARD.value:
                        self._set_level(LockdownLevel.HARD)

            elif event.state_after == PresenceState.ABSENT:
                if self._absence_start is None:
                    self._absence_start = now
                absence_duration = now - self._absence_start
                if absence_duration >= self.medium_timeout:
                    self._set_level(LockdownLevel.MEDIUM)
                elif absence_duration >= self.soft_timeout:
                    self._set_level(LockdownLevel.SOFT)

            elif event.state_after == PresenceState.UNAUTHORIZED:
                self._threat_detected_at = now
                if self._current_level.value < LockdownLevel.CRITICAL.value:
                    self._set_level(LockdownLevel.CRITICAL)

            elif event.state_after == PresenceState.MULTIPLE:
                if event.unknown_faces > 0:
                    self._set_level(LockdownLevel.HARD)

            self._history.append({
                "timestamp": datetime.utcnow().isoformat(),
                "event_type": event.event_type,
                "level_before": old_level.name,
                "level_after": self._current_level.name,
            })

            if self._current_level != old_level and self._on_change:
                self._on_change(old_level, self._current_level)

    def _set_level(self, level: LockdownLevel) -> None:
        """Set lockdown level and execute associated actions."""
        if level == self._current_level:
            return

        old = self._current_level
        self._current_level = level
        self._level_since = time.time()

        action = {
            "timestamp": datetime.utcnow().isoformat(),
            "from_level": old.name,
            "to_level": level.name,
            "actions": [],
        }

        if level == LockdownLevel.SOFT:
            action["actions"] = ["dim_display", "pause_non_critical"]
        elif level == LockdownLevel.MEDIUM:
            action["actions"] = ["lock_screen", "require_auth", "suspend_agents"]
        elif level == LockdownLevel.HARD:
            action["actions"] = ["encrypt_sensitive_data", "disable_network_shares", "alert_admin"]
        elif level == LockdownLevel.CRITICAL:
            action["actions"] = [
                "full_lockdown", "capture_evidence", "alert_all",
                "disable_external_network", "engage_kill_switch_standby",
            ]
        elif level == LockdownLevel.NONE:
            action["actions"] = ["restore_normal_operations"]

        self._actions_log.append(action)
        logger.info(f"Lockdown level changed: {old.name} → {level.name}")

    def force_lockdown(self, level: LockdownLevel) -> None:
        """Force a specific lockdown level."""
        with self._lock:
            self._set_level(level)

    def release_lockdown(self) -> None:
        """Release all lockdown measures."""
        with self._lock:
            self._set_level(LockdownLevel.NONE)
            self._absence_start = None
            self._threat_detected_at = None

    @property
    def current_level(self) -> LockdownLevel:
        with self._lock:
            return self._current_level

    @property
    def level_duration(self) -> float:
        return time.time() - self._level_since

    def get_status(self) -> Dict[str, Any]:
        with self._lock:
            return {
                "level": self._current_level.name,
                "level_value": self._current_level.value,
                "level_duration_seconds": round(time.time() - self._level_since, 1),
                "absence_duration": round(time.time() - self._absence_start, 1) if self._absence_start else None,
                "threat_active": self._threat_detected_at is not None,
                "history_count": len(self._history),
                "actions_count": len(self._actions_log),
            }


