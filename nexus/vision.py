class CameraManager:
    """Manages webcam hardware and frame capture.

    Handles camera initialization, frame capture, resolution management,
    and graceful hardware error recovery.
    """

    def __init__(
        self,
        camera_index: int = 0,
        width: int = 640,
        height: int = 480,
        fps: int = 30,
    ):
        self.camera_index = camera_index
        self.width = width
        self.height = height
        self.fps = fps
        self._capture = None
        self._state = CameraState.DISCONNECTED
        self._frame_count = 0
        self._last_frame_time = 0.0
        self._lock = threading.Lock()
        logger.info(f"CameraManager created for camera {camera_index} ({width}x{height}@{fps}fps)")

    def open(self) -> bool:
        """Open the camera device."""
        try:
            try:
                import cv2
                self._capture = cv2.VideoCapture(self.camera_index)
                if self._capture.isOpened():
                    self._capture.set(cv2.CAP_PROP_FRAME_WIDTH, self.width)
                    self._capture.set(cv2.CAP_PROP_FRAME_HEIGHT, self.height)
                    self._capture.set(cv2.CAP_PROP_FPS, self.fps)
                    self._state = CameraState.STREAMING
                    logger.info("Camera opened successfully via OpenCV")
                    return True
                else:
                    self._state = CameraState.ERROR
                    return False
            except ImportError:
                logger.warning("OpenCV not available, camera in simulation mode")
                self._state = CameraState.CONNECTED
                return True
        except Exception as e:
            logger.error(f"Camera open failed: {e}")
            self._state = CameraState.ERROR
            return False

    def read_frame(self) -> Optional[List[List[List[int]]]]:
        """Read a frame from the camera.

        Returns frame as 3D list [rows][cols][rgb] or None on failure.
        """
        if self._capture is not None:
            try:
                import cv2
                ret, frame = self._capture.read()
                if ret:
                    self._frame_count += 1
                    self._last_frame_time = time.time()
                    # Convert numpy to list for our pipeline
                    return frame.tolist()
            except Exception:
                pass

        # Simulation mode — generate a synthetic frame
        self._frame_count += 1
        self._last_frame_time = time.time()
        frame = []
        for r in range(self.height // 10):
            row = []
            for c in range(self.width // 10):
                # Generate varying pattern
                v = int(128 + 50 * math.sin(r * 0.1 + self._frame_count * 0.01))
                row.append([v, v + 10, v - 10])
            frame.append(row)
        return frame

    def close(self) -> None:
        """Release camera resources."""
        if self._capture is not None:
            try:
                self._capture.release()
            except Exception:
                pass
            self._capture = None
        self._state = CameraState.DISCONNECTED
        logger.info("Camera released")

    @property
    def state(self) -> CameraState:
        return self._state

    @property
    def frame_count(self) -> int:
        return self._frame_count

    @property
    def is_open(self) -> bool:
        return self._state in (CameraState.CONNECTED, CameraState.STREAMING)


