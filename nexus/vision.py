# ═══════════════════════════════════════════════════════════════════════════

class VisionPresenceDetector:
    """Main presence detection engine combining all vision subsystems.

    Orchestrates camera capture, motion detection, face recognition,
    and gesture recognition into a unified presence detection pipeline.
    """

    def __init__(
        self,
        camera_index: int = 0,
        detection_interval: float = 1.0,
        absence_threshold: float = 30.0,
        on_event: Optional[Callable[[PresenceEvent], None]] = None,
    ):
        self.camera = CameraManager(camera_index=camera_index)
        self.frame_processor = FrameProcessor()
        self.motion_detector = MotionDetector()
        self.face_recognizer = FaceRecognizer()
        self.gesture_recognizer = GestureRecognizer()
        self.lockdown = LockdownManager()

        self.detection_interval = detection_interval
        self.absence_threshold = absence_threshold
        self._on_event = on_event

        self._current_state = PresenceState.UNKNOWN
        self._last_presence_time = 0.0
        self._is_running = False
        self._worker_thread: Optional[threading.Thread] = None
        self._event_history: deque = deque(maxlen=500)
        self._lock = threading.Lock()
        logger.info("VisionPresenceDetector initialized")

    def start(self) -> bool:
        """Start the presence detection pipeline."""
        if not self.camera.open():
            logger.error("Failed to open camera")
            return False
        self._is_running = True
        self._worker_thread = threading.Thread(target=self._detection_loop, daemon=True)
        self._worker_thread.start()
        logger.info("Presence detection started")
        return True

    def stop(self) -> None:
        """Stop the presence detection pipeline."""
        self._is_running = False
        if self._worker_thread:
            self._worker_thread.join(timeout=5.0)
        self.camera.close()
        logger.info("Presence detection stopped")

    def _detection_loop(self) -> None:
        """Main detection loop running in background thread."""
        while self._is_running:
            try:
                frame = self.camera.read_frame()
                if frame is None:
                    time.sleep(0.1)
                    continue

                # Process frame
                processed = self.frame_processor.preprocess(frame)

                # Detect motion
                motion, motion_level, motion_regions = self.motion_detector.process_frame(processed)

                # Face recognition
                faces = self.face_recognizer.recognize_faces(processed)

                # Determine presence state
                old_state = self._current_state
                new_state = self._determine_state(motion, faces)

                if new_state != old_state:
                    event = PresenceEvent(
                        event_type=self._get_event_type(old_state, new_state),
                        state_before=old_state,
                        state_after=new_state,
                        face_count=len(faces),
                        recognized_users=[name for _, name, _ in faces if name],
                        unknown_faces=sum(1 for _, name, _ in faces if name is None),
                        bounding_boxes=[box for box, _, _ in faces],
                        frame_hash=self.frame_processor.compute_frame_hash(processed),
                    )
                    self._emit_event(event)

                with self._lock:
                    self._current_state = new_state

                if new_state == PresenceState.PRESENT:
                    self._last_presence_time = time.time()

                # Check absence timeout
                if (new_state != PresenceState.PRESENT and
                    self._last_presence_time > 0 and
                    time.time() - self._last_presence_time > self.absence_threshold):
                    if self._current_state != PresenceState.ABSENT:
                        with self._lock:
                            self._current_state = PresenceState.ABSENT
                        event = PresenceEvent(
                            event_type="timeout_absent",
                            state_before=old_state,
                            state_after=PresenceState.ABSENT,
                        )
                        self._emit_event(event)

                time.sleep(self.detection_interval)

            except Exception as e:
                logger.error(f"Detection loop error: {e}")
                time.sleep(1.0)

    def _determine_state(self, motion: bool, faces: List[Tuple]) -> PresenceState:
        """Determine presence state from detection results."""
        if not faces and not motion:
            return self._current_state  # No change without evidence

        recognized = [name for _, name, _ in faces if name]
        unknown = sum(1 for _, name, _ in faces if name is None)

        if recognized and unknown == 0:
            return PresenceState.PRESENT
        elif unknown > 0 and not recognized:
            return PresenceState.UNAUTHORIZED
        elif recognized and unknown > 0:
            return PresenceState.MULTIPLE
        elif motion:
            return PresenceState.TRANSITIONING
        return self._current_state

    def _get_event_type(self, old: PresenceState, new: PresenceState) -> str:
        if new == PresenceState.PRESENT and old != PresenceState.PRESENT:
            return "entered"
        elif new == PresenceState.ABSENT:
            return "left"
        elif new == PresenceState.UNAUTHORIZED:
            return "unauthorized"
        elif new == PresenceState.MULTIPLE:
            return "multiple_detected"
        return "state_change"

    def _emit_event(self, event: PresenceEvent) -> None:
        """Emit a presence event to handlers."""
        self._event_history.append(event)
        self.lockdown.on_presence_event(event)
        if self._on_event:
            try:
                self._on_event(event)
            except Exception as e:
                logger.error(f"Presence event callback error: {e}")

    @property
    def current_state(self) -> PresenceState:
        with self._lock:
            return self._current_state

    @property
    def is_running(self) -> bool:
        return self._is_running

    def get_status(self) -> Dict[str, Any]:
        return {
            "is_running": self._is_running,
            "current_state": self._current_state.value,
            "camera_state": self.camera.state.value,
            "frames_processed": self.camera.frame_count,
            "lockdown": self.lockdown.get_status(),
            "motion": self.motion_detector.get_motion_summary(),
            "face_recognizer": self.face_recognizer.stats,
            "event_count": len(self._event_history),
        }

    def get_recent_events(self, limit: int = 20) -> List[Dict[str, Any]]:
        return [e.to_dict() for e in list(self._event_history)[-limit:]]


