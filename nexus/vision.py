class VisionPipeline:
    """High-level vision pipeline combining all detection subsystems.

    Provides a single entry point for frame processing that outputs
    structured detection results from all active analyzers.
    """

    def __init__(self, config: Optional[Dict[str, Any]] = None):
        config = config or {}
        self.presence = VisionPresenceDetector(
            camera_index=config.get("camera_index", 0),
            detection_interval=config.get("detection_interval", 1.0),
        )
        self._analyzers: List[Callable] = []
        self._is_active = False

    def add_analyzer(self, analyzer: Callable) -> None:
        self._analyzers.append(analyzer)

    def start(self) -> bool:
        self._is_active = True
        return self.presence.start()

    def stop(self) -> None:
        self._is_active = False
        self.presence.stop()

    def get_full_status(self) -> Dict[str, Any]:
        return {
            "pipeline_active": self._is_active,
            "analyzers": len(self._analyzers),
            "presence": self.presence.get_status(),
        }
