class MotionDetector:
    """Detects and tracks motion in video frames.

    Uses frame differencing and background subtraction to identify
    moving objects and estimate motion vectors.
    """

    def __init__(
        self,
        sensitivity: float = 0.5,
        min_area_ratio: float = 0.01,
        max_still_frames: int = 30,
    ):
        self.sensitivity = max(0.1, min(sensitivity, 1.0))
        self.min_area_ratio = min_area_ratio
        self.max_still_frames = max_still_frames
        self._processor = FrameProcessor()
        self._prev_gray: Optional[List[List[int]]] = None
        self._still_frame_count = 0
        self._motion_history: deque = deque(maxlen=100)
        self._is_motion_detected = False
        self._lock = threading.Lock()

    def process_frame(self, frame: List[List[List[int]]]) -> Tuple[bool, float, List[BoundingBox]]:
        """Process a frame and detect motion.

        Returns (motion_detected, motion_level, motion_regions).
        """
        gray = self._processor.to_grayscale(frame)
        self._processor.update_background(gray)

        if self._prev_gray is None:
            self._prev_gray = gray
            return (False, 0.0, [])

        # Compute frame difference
        diff_level = self._processor.compute_difference(gray, self._prev_gray)
        threshold = (1.0 - self.sensitivity) * 0.1  # Scale threshold by sensitivity
        motion_detected = diff_level > threshold

        # Get motion regions from foreground mask
        regions = []
        if motion_detected:
            mask = self._processor.get_foreground_mask(gray)
            regions = self._find_motion_regions(mask)
            self._still_frame_count = 0
        else:
            self._still_frame_count += 1

        with self._lock:
            self._is_motion_detected = motion_detected
            self._motion_history.append({
                "timestamp": time.time(),
                "level": diff_level,
                "detected": motion_detected,
                "regions": len(regions),
            })

        self._prev_gray = gray
        return (motion_detected, diff_level, regions)

    def _find_motion_regions(self, mask: List[List[bool]]) -> List[BoundingBox]:
        """Find connected regions of motion in foreground mask."""
        if not mask or not mask[0]:
            return []

        height = len(mask)
        width = len(mask[0])
        total_pixels = height * width
        min_area = int(total_pixels * self.min_area_ratio)

        # Simple region detection: scan for clusters of True pixels
        visited = [[False] * width for _ in range(height)]
        regions = []

        for r in range(0, height, 4):  # Sample every 4th row for speed
            for c in range(0, width, 4):
                if r < len(mask) and c < len(mask[r]) and mask[r][c] and not visited[r][c]:
                    # Flood fill to find region bounds
                    min_r, max_r, min_c, max_c = r, r, c, c
                    area = 0
                    stack = [(r, c)]
                    while stack:
                        cr, cc = stack.pop()
                        if (cr < 0 or cr >= height or cc < 0 or cc >= width
                                or visited[cr][cc] or not mask[cr][cc]):
                            continue
                        visited[cr][cc] = True
                        area += 1
                        min_r = min(min_r, cr)
                        max_r = max(max_r, cr)
                        min_c = min(min_c, cc)
                        max_c = max(max_c, cc)
                        # Only explore in steps to limit complexity
                        for dr, dc in [(4, 0), (-4, 0), (0, 4), (0, -4)]:
                            stack.append((cr + dr, cc + dc))

                    if area >= min_area // 16:  # Adjusted for sampling
                        regions.append(BoundingBox(
                            x=min_c, y=min_r,
                            width=max_c - min_c, height=max_r - min_r,
                            confidence=min(1.0, area / (total_pixels * 0.1)),
                            label="motion",
                        ))

        return regions

    @property
    def is_motion_detected(self) -> bool:
        with self._lock:
            return self._is_motion_detected

    @property
    def is_scene_still(self) -> bool:
        return self._still_frame_count >= self.max_still_frames

    def get_motion_summary(self) -> Dict[str, Any]:
        with self._lock:
            recent = list(self._motion_history)
        if not recent:
            return {"avg_level": 0, "motion_ratio": 0, "samples": 0}
        avg = sum(m["level"] for m in recent) / len(recent)
        motion_count = sum(1 for m in recent if m["detected"])
        return {
            "avg_level": round(avg, 4),
            "motion_ratio": round(motion_count / len(recent), 3),
            "samples": len(recent),
            "still_frames": self._still_frame_count,
        }


