class FrameProcessor:
    """Processes video frames for analysis.

    Handles frame scaling, color conversion, region-of-interest extraction,
    and frame differencing for motion detection.
    """

    def __init__(self, target_width: int = 640, target_height: int = 480):
        self.target_width = target_width
        self.target_height = target_height
        self._frame_count = 0
        self._last_frame: Optional[List[List[int]]] = None
        self._background_model: Optional[List[List[float]]] = None
        self._alpha = 0.05  # Background model learning rate

    def preprocess(self, frame: List[List[List[int]]]) -> List[List[List[int]]]:
        """Preprocess a frame for analysis (resize, normalize)."""
        self._frame_count += 1
        # In real implementation, this would use cv2.resize etc.
        # Here we work with the frame as-is for the logic layer
        return frame

    def to_grayscale(self, frame: List[List[List[int]]]) -> List[List[int]]:
        """Convert RGB frame to grayscale."""
        gray = []
        for row in frame:
            gray_row = []
            for pixel in row:
                if len(pixel) >= 3:
                    # Luminance formula
                    g = int(0.299 * pixel[0] + 0.587 * pixel[1] + 0.114 * pixel[2])
                    gray_row.append(g)
                else:
                    gray_row.append(pixel[0] if pixel else 0)
            gray.append(gray_row)
        return gray

    def compute_frame_hash(self, frame: List[List[List[int]]]) -> str:
        """Compute perceptual hash of a frame for deduplication."""
        # Simple average hash approach
        h = hashlib.sha256()
        for row in frame[:10]:  # Sample first 10 rows
            for pixel in row[:10]:
                for v in pixel:
                    h.update(struct.pack("B", v & 0xFF))
        return h.hexdigest()[:16]

    def compute_difference(self, frame1: List[List[int]], frame2: List[List[int]]) -> float:
        """Compute normalized pixel difference between two grayscale frames."""
        if not frame1 or not frame2:
            return 1.0
        total_diff = 0
        pixel_count = 0
        min_rows = min(len(frame1), len(frame2))
        for r in range(min_rows):
            min_cols = min(len(frame1[r]), len(frame2[r]))
            for c in range(min_cols):
                total_diff += abs(frame1[r][c] - frame2[r][c])
                pixel_count += 1
        return total_diff / (pixel_count * 255) if pixel_count > 0 else 0.0

    def update_background(self, gray_frame: List[List[int]]) -> None:
        """Update running background model."""
        if self._background_model is None:
            self._background_model = [[float(v) for v in row] for row in gray_frame]
            return
        for r in range(min(len(self._background_model), len(gray_frame))):
            for c in range(min(len(self._background_model[r]), len(gray_frame[r]))):
                self._background_model[r][c] = (
                    (1 - self._alpha) * self._background_model[r][c]
                    + self._alpha * gray_frame[r][c]
                )

    def get_foreground_mask(self, gray_frame: List[List[int]], threshold: int = 30) -> List[List[bool]]:
        """Get foreground mask by background subtraction."""
        if self._background_model is None:
            return [[False] * len(row) for row in gray_frame]
        mask = []
        for r in range(min(len(self._background_model), len(gray_frame))):
            row_mask = []
            for c in range(min(len(self._background_model[r]), len(gray_frame[r]))):
                diff = abs(gray_frame[r][c] - self._background_model[r][c])
                row_mask.append(diff > threshold)
            mask.append(row_mask)
        return mask

    @property
    def frame_count(self) -> int:
        return self._frame_count


